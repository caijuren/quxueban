// ============================================
// API 函数
// ============================================
async function fetchReports(type?: ReportType, childId?: number): Promise<Report[]> {
  const params = new URLSearchParams();
  if (type) params.append('type', type);
  if (childId) params.append('childId', childId.toString());
  const { data } = await apiClient.get(`/reports?${params}`);
  return data.data;
}

async function fetchReport(id: number): Promise<Report> {
  const { data } = await apiClient.get(`/reports/${id}`);
  return data.data;
}

async function generateReport(params: {
  type: ReportType;
  childId?: number;
  startDate: string;
  endDate: string;
  subjects?: string[];
}): Promise<Report> {
  const { data } = await apiClient.post('/reports/generate', params);
  return data.data;
}

async function deleteReport(id: number): Promise<void> {
  await apiClient.delete(`/reports/${id}`);
}

async function toggleFavorite(id: number, isFavorite: boolean): Promise<void> {
  await apiClient.put(`/reports/${id}/favorite`, { isFavorite });
}

async function fetchChildren(): Promise<Child[]> {
  const { data } = await apiClient.get('/children');
  return data.data;
}

