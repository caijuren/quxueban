function GenerateReportForm({
  onGenerate,
  isGenerating,
}: {
  onGenerate: (params: {
    type: ReportType;
    childId?: number;
    startDate: string;
    endDate: string;
  }) => void;
  isGenerating: boolean;
}) {
  const [selectedType, setSelectedType] = useState<ReportType>('weekly');
  const [selectedChild, setSelectedChild] = useState<number | undefined>();
  const [dateRange, setDateRange] = useState({
    start: new Date(Date.now() - 7 * 24 * 60 * 60 * 1000).toISOString().split('T')[0],
    end: new Date().toISOString().split('T')[0],
  });

  const { data: children } = useQuery({
    queryKey: ['children'],
    queryFn: fetchChildren,
  });

  const handleGenerate = () => {
    onGenerate({
      type: selectedType,
      childId: selectedChild,
      startDate: dateRange.start,
      endDate: dateRange.end,
    });
  };

  // 根据报告类型自动设置日期范围
  useEffect(() => {
    const now = new Date();
    let start = new Date();

    switch (selectedType) {
      case 'weekly':
        start = new Date(now.getTime() - 7 * 24 * 60 * 60 * 1000);
        break;
      case 'monthly':
        start = new Date(now.getFullYear(), now.getMonth() - 1, now.getDate());
        break;
      case 'semester':
        start = new Date(now.getFullYear(), now.getMonth() - 3, now.getDate());
        break;
      default:
        break;
    }

    setDateRange({
      start: start.toISOString().split('T')[0],
      end: now.toISOString().split('T')[0],
    });
  }, [selectedType]);

  return (
    <div className="space-y-6">
      {/* 报告类型选择 */}
      <div>
        <h4 className="text-sm font-medium text-gray-700 mb-3">选择报告类型</h4>
        <div className="grid grid-cols-2 gap-3">
          {REPORT_TYPES.map((type) => (
            <button
              key={type.id}
              onClick={() => setSelectedType(type.id)}
              className={cn(
                'flex items-start gap-3 p-4 rounded-xl border-2 transition-all text-left',
                selectedType === type.id
                  ? 'border-primary bg-primary/5'
                  : 'border-gray-200 hover:border-gray-300'
              )}
            >
              <div
                className={cn(
                  'w-10 h-10 rounded-lg flex items-center justify-center text-white shrink-0',
                  type.color
                )}
              >
                {type.icon}
              </div>
              <div>
                <div className="flex items-center gap-2">
                  <span className="font-medium">{type.name}</span>
                  {type.autoGenerate && (
                    <Badge variant="secondary" className="text-xs">
                      自动
                    </Badge>
                  )}
                </div>
                <p className="text-xs text-gray-500 mt-1">{type.description}</p>
              </div>
            </button>
          ))}
        </div>
      </div>

      {/* 孩子选择 */}
      {children && children.length > 0 && (
        <div>
          <h4 className="text-sm font-medium text-gray-700 mb-3">选择孩子</h4>
          <div className="flex flex-wrap gap-2">
            <button
              onClick={() => setSelectedChild(undefined)}
              className={cn(
                'px-4 py-2 rounded-full text-sm font-medium transition-all',
                !selectedChild
                  ? 'bg-primary text-white'
                  : 'bg-gray-100 text-gray-700 hover:bg-gray-200'
              )}
            >
              全部
            </button>
            {children.map((child) => (
              <button
                key={child.id}
                onClick={() => setSelectedChild(child.id)}
                className={cn(
                  'px-4 py-2 rounded-full text-sm font-medium transition-all flex items-center gap-2',
                  selectedChild === child.id
                    ? 'bg-primary text-white'
                    : 'bg-gray-100 text-gray-700 hover:bg-gray-200'
                )}
              >
                <span>{child.avatar}</span>
                <span>{child.name}</span>
              </button>
            ))}
          </div>
        </div>
      )}

      {/* 时间范围 */}
      <div>
        <h4 className="text-sm font-medium text-gray-700 mb-3">时间范围</h4>
        <div className="flex gap-3">
          <input
            type="date"
            value={dateRange.start}
            onChange={(e) => setDateRange({ ...dateRange, start: e.target.value })}
            className="flex-1 px-3 py-2 border rounded-lg text-sm"
          />
          <span className="text-gray-400 self-center">至</span>
          <input
            type="date"
            value={dateRange.end}
            onChange={(e) => setDateRange({ ...dateRange, end: e.target.value })}
            className="flex-1 px-3 py-2 border rounded-lg text-sm"
          />
        </div>
      </div>

      {/* 生成按钮 */}
      <Button
        onClick={handleGenerate}
        disabled={isGenerating}
        className="w-full"
        size="lg"
      >
        {isGenerating ? (
          <>
            <Loader2 className="w-5 h-5 mr-2 animate-spin" />
            AI 分析中...
          </>
        ) : (
          <>
            <Sparkles className="w-5 h-5 mr-2" />
            生成 AI 分析报告
          </>
        )}
      </Button>
    </div>
  );
}

